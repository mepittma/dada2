# # # # # # # # SUBROUTINE FOR DOWNLOADING DATA # # # # # # #
sub download_data {
    my ($run_ref, $path) = @_;
    my @run_list = @$run_ref;

    print(join(", ",@run_list), "\n");
    print($path,"\n");

    # If the path doesn't exist, create it
    if ( !-d $path ) {
        make_path $path or die "Failed to create path: $path";
    }

    foreach my $run(@run_list) {
        print "Run ID: $run\n";
        $cmd = "fastq-dump --outdir $path --gzip --skip-technical  --readids --read-filter pass --dumpbase --split-files --clip $run";
        system($cmd);
    }
}
